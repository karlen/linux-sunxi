static unsigned long calc_rate_pll1(u32 parent_rate, struct clk_factors_value *factor)
{
    return (unsigned long)(parent_rate?parent_rate:24000000) * (factor->factorn) / (factor->factorp?4:1);
}

static unsigned long calc_rate_pll3(u32 parent_rate, struct clk_factors_value *factor)
{
    if((factor->factorn == 39) && (factor->factord2 == 1) && (factor->factorp == 20))
        return 22579200;
    else if((factor->factorn == 43) && (factor->factord2 == 1) && (factor->factorp == 20))
        return 24576000;
    else
    return (unsigned long)(parent_rate?parent_rate:24000000) * (factor->factorn+1) \
    	/((factor->factord1+1)*(factor->factord2+1)*(factor->factorp+1));	
}

static unsigned long calc_rate_media(u32 parent_rate, struct clk_factors_value *factor)
{
    return (unsigned long)(parent_rate?parent_rate:24000000) * (factor->factorn) \
    	/((factor->factord1+1)*(factor->factord2+1));	
}
static unsigned long calc_rate_pll7(u32 parent_rate, struct clk_factors_value *factor)
{
    return (unsigned long)(parent_rate?parent_rate:24000000) * (factor->factorn) \
    	/((factor->factord1+1));	
}
static unsigned long calc_rate_pll8(u32 parent_rate, struct clk_factors_value *factor)
{
    return (unsigned long)(parent_rate?parent_rate:24000000) * (factor->factorn) \
    	/((factor->factord1+1)*(1+factor->factorp?((factor->factorp>1)?3:1):0));	
}

